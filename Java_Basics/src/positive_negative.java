
public class positive_negative {
	public static void main(String[]args) {
		int number = 0;
		if (number>0) {
			System.out.println("Number is positive" +number);
		}
		else if (number<0) {
			System.out.println("Number is negative" +number);
		}
		else {
			System.out.println("Number is zero");
		}
	}

}
